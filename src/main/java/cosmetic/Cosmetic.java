package cosmetic;

public class Cosmetic {
	private int cosmeticID;
	private String cosmeticKind;
	private String cosmeticName;
	private String cosmeticPrice;
	private String cosmeticCount;
	private String cosmeticImage;
	private String cosmeticContent;
	private int cosmeticAvailable;
	
	public int getCosmeticID() {
		return cosmeticID;
	}
	public void setCosmeticID(int cosmeticID) {
		this.cosmeticID = cosmeticID;
	}
	public String getCosmeticKind() {
		return cosmeticKind;
	}
	public void setCosmeticKind(String cosmeticKind) {
		this.cosmeticKind = cosmeticKind;
	}
	public String getCosmeticName() {
		return cosmeticName;
	}
	public void setCosmeticName(String cosmeticName) {
		this.cosmeticName = cosmeticName;
	}
	public String getCosmeticPrice() {
		return cosmeticPrice;
	}
	public void setCosmeticPrice(String cosmeticPrice) {
		this.cosmeticPrice = cosmeticPrice;
	}
	public String getCosmeticCount() {
		return cosmeticCount;
	}
	public void setCosmeticCount(String cosmeticCount) {
		this.cosmeticCount = cosmeticCount;
	}
	public String getCosmeticImage() {
		return cosmeticImage;
	}
	public void setCosmeticImage(String cosmeticImage) {
		this.cosmeticImage = cosmeticImage;
	}
	public String getCosmeticContent() {
		return cosmeticContent;
	}
	public void setCosmeticContent(String cosmeticContent) {
		this.cosmeticContent = cosmeticContent;
	}
	public int getCosmeticAvailable() {
		return cosmeticAvailable;
	}
	public void setCosmeticAvailable(int cosmeticAvailable) {
		this.cosmeticAvailable = cosmeticAvailable;
	}
	
	
}
